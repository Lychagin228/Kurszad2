/**
 * analysis.h - Заголовочный файл для программы анализа кусочной функции
 * Функции для анализа функции f(x)
 *
 * f(x) = {
 *   arctan(2x),         x < -2
 *   (e^x - x - 1)/x^2, -2 <= x < 0
 *   sqrt(x^2 + 4),      x >= 0
 * }
 */

#ifndef ANALYSIS_H
#define ANALYSIS_H

 /**
  * Вычисление значения кусочной функции f(x)
  * @param x аргумент функции
  * @return значение функции f(x) в точке x
  */
double f(double x);

/**
 * Вычисление значения функции в точке (обертка для меню)
 * @param x аргумент функции
 * @return значение функции f(x) в точке x
 */
double tochka(double x);

/**
 * Вывод таблицы значений функции на интервале
 * @param start начало интервала
 * @param end конец интервала
 * @param step шаг табуляции
 * @return 0 в случае успешного выполнения
 */
int tablica(double start, double end, double step);

/**
 * Вычисление определенного интеграла методом прямоугольников
 * @param a нижний предел интегрирования
 * @param b верхний предел интегрирования
 * @param n параметр, обратный количеству разбиений (шаг = (b-a)*n)
 * @return приближенное значение интеграла ?[a,b] f(x) dx
 */
double integral(double a, double b, double n);

/**
 * Поиск аргумента x по заданному значению y
 * @param y целевое значение функции
 * @param eps допустимая погрешность
 * @return найденное значение x или 0.0 если не найдено
 */
double poisk_x(double y, double eps);

/**
 * Вычисление производной функции в точке методом конечных разностей
 * @param x точка, в которой вычисляется производная
 * @return приближенное значение производной f'(x)
 */
double proizvodnaya(double x);

/**
 * Вывод главного меню программы
 * @return 0 в случае успешного выполнения
 */
int menu();

#endif // ANALYSIS_H